const Palette& builtinPalette(BuiltinPalette palette)
{
    static const Palette rainbow(rainbowSlots);
    static const Palette redWhiteBlue(redWhiteBlueSlots);
    static const Palette viridis(viridisSlots);
    static const Palette vort(vortSlots);
    static const Palette blueFlame(blueFlameSlots);
    switch (palette) {
    case BuiltinPalette::Rainbow: return rainbow;
    case BuiltinPalette::RedWhiteBlue: return redWhiteBlue;
    case BuiltinPalette::Viridis: return viridis;
    case BuiltinPalette::Vort: return vort;
    case BuiltinPalette::BlueFlame: return blueFlame;
    }
    return rainbow;
}

std::string_view builtinPaletteName(BuiltinPalette palette) noexcept
{
    switch (palette) {
    case BuiltinPalette::Rainbow: return "rainbow.pal";
    case BuiltinPalette::RedWhiteBlue: return "rwb.pal";
    case BuiltinPalette::Viridis: return "viridis.pal";
    case BuiltinPalette::Vort: return "vort.pal";
    case BuiltinPalette::BlueFlame: return "blueflame4.pal";
    }
    return "rainbow";
}

